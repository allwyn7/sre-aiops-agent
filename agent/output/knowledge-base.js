const KB_PATH = 'knowledge-base/incidents.md';

export async function appendToKnowledgeBase(github, { alert, diagnosis, prUrl, issueUrl }) {
  const { diagnosis: d, runbook_entry: rb } = diagnosis;

  const existing = await github.getFileContent(KB_PATH);
  const currentContent = existing?.content ?? '# Incident Knowledge Base\n\nResolved incidents and runbook entries generated by the SRE AIOps Agent.\n\n---\n\n';

  const entry = [
    `## ${alert.incident_id} — ${new Date(alert.timestamp).toISOString().split('T')[0]}`,
    '',
    `**Title:** ${alert.title}`,
    `**Severity:** ${alert.severity} | **Service:** \`${alert.service}\``,
    '',
    `### Pattern`,
    `\`${rb.pattern}\``,
    '',
    `### Root Cause`,
    d.root_cause,
    '',
    `### Resolution`,
    rb.resolution,
    '',
    `**Post-Incident Issue:** ${issueUrl}`,
    `**Fix PR:** ${prUrl}`,
    '',
    '---',
    '',
  ].join('\n');

  await github.updateFile({
    path:    KB_PATH,
    content: currentContent + entry,
    message: `docs: add runbook entry for ${alert.incident_id}`,
  });
}
