/**
 * Commits SRE artifacts (runbook, alerting rules, recommendations) to the knowledge base.
 */

export async function commitSREArtifacts(github, { alert, sreArtifacts, issueUrl, prUrl }) {
  if (!sreArtifacts) {
    console.log('   Skipping SRE artifact commits (no artifacts generated).');
    return { runbookPath: null, alertingRulesPath: null, recommendationsPath: null };
  }

  const idLower = alert.incident_id.toLowerCase().replace(/_/g, '-');
  const date = new Date(alert.timestamp).toISOString().split('T')[0];
  const paths = {
    runbookPath:          `knowledge-base/runbooks/${idLower}.md`,
    alertingRulesPath:    `knowledge-base/alerting-rules/${idLower}.yml`,
    recommendationsPath:  `knowledge-base/recommendations/${idLower}.md`,
  };

  // 1. Commit runbook ────────────────────────────────────────────────────────
  const rb = sreArtifacts.runbook;
  if (rb) {
    const runbookContent = [
      `# Runbook: ${rb.title}`,
      '',
      `> **Incident:** ${alert.incident_id} | **Service:** \`${alert.service}\` | **Date:** ${date}`,
      '',
      `**Trigger Pattern:** \`${rb.incident_pattern}\``,
      '',
      '---',
      '',
      '## 1. Detection',
      '',
      rb.detection,
      '',
      '## 2. Triage',
      '',
      rb.triage,
      '',
      '## 3. Diagnosis',
      '',
      rb.diagnosis,
      '',
      '## 4. Resolution',
      '',
      rb.resolution,
      '',
      '## 5. Verification',
      '',
      rb.verification,
      '',
      '## 6. Prevention',
      '',
      rb.prevention,
      '',
      '---',
      '',
      `**Post-Incident Issue:** ${issueUrl}`,
      `**Fix PR:** ${prUrl}`,
      '',
      `_Generated by SRE AIOps Agent_`,
      '',
    ].join('\n');

    try {
      await github.updateFile({
        path:    paths.runbookPath,
        content: runbookContent,
        message: `docs: add runbook for ${alert.incident_id}`,
      });
    } catch (err) {
      console.warn(`   Warning: failed to commit runbook: ${err.message}`);
    }
  }

  // 2. Commit alerting rules ─────────────────────────────────────────────────
  const alertingYaml = sreArtifacts.alerting_rules_yaml;
  if (alertingYaml) {
    const alertingContent = [
      `# Alerting rules generated by SRE AIOps Agent`,
      `# Incident: ${alert.incident_id} — ${alert.title}`,
      `# Date: ${date}`,
      '',
      alertingYaml,
      '',
    ].join('\n');

    try {
      await github.updateFile({
        path:    paths.alertingRulesPath,
        content: alertingContent,
        message: `monitoring: add alerting rules for ${alert.incident_id}`,
      });
    } catch (err) {
      console.warn(`   Warning: failed to commit alerting rules: ${err.message}`);
    }
  }

  // 3. Commit recommendations (capacity + DR + performance) ──────────────────
  const cp = sreArtifacts.capacity_planning;
  const dr = sreArtifacts.disaster_recovery;
  const pt = sreArtifacts.performance_tuning;

  if (cp || dr || pt) {
    const sections = [
      `# SRE Recommendations: ${alert.incident_id}`,
      '',
      `> **Service:** \`${alert.service}\` | **Severity:** ${alert.severity} | **Date:** ${date}`,
      '',
      '---',
      '',
    ];

    if (cp) {
      sections.push(
        '## Capacity Planning',
        '',
        '### Current Utilization',
        cp.current_utilization,
        '',
        '### Scaling Thresholds',
        cp.scaling_thresholds,
        '',
        '### Resource Recommendations',
        cp.resource_recommendations,
        '',
        '### Growth Projections',
        cp.growth_projections,
        '',
        '### Cost Considerations',
        cp.cost_considerations,
        '',
        '---',
        '',
      );
    }

    if (dr) {
      sections.push(
        '## Disaster Recovery Assessment',
        '',
        '### Single Points of Failure',
        ...(Array.isArray(dr.spof_identified) ? dr.spof_identified.map(s => `- ${s}`) : [dr.spof_identified]),
        '',
        '### High Availability Recommendations',
        dr.ha_recommendations,
        '',
        '### Failover Strategy',
        dr.failover_strategy,
        '',
        '### RTO / RPO Assessment',
        dr.rto_rpo_assessment,
        '',
        '### Backup Recommendations',
        dr.backup_recommendations,
        '',
        '---',
        '',
      );
    }

    if (pt) {
      sections.push(
        '## Performance Tuning',
        '',
        '### JVM Tuning',
        pt.jvm_tuning,
        '',
        '### Spring Boot Configuration',
        pt.spring_boot_tuning,
        '',
        '### JPA / Hibernate Tuning',
        pt.jpa_hibernate_tuning,
        '',
        '### Database (PostgreSQL) Tuning',
        pt.database_tuning,
        '',
        '### Connection Pool (HikariCP) Tuning',
        pt.connection_pool_tuning,
        '',
        '---',
        '',
      );
    }

    sections.push(
      `**Post-Incident Issue:** ${issueUrl}`,
      `**Fix PR:** ${prUrl}`,
      '',
      `_Generated by SRE AIOps Agent_`,
      '',
    );

    try {
      await github.updateFile({
        path:    paths.recommendationsPath,
        content: sections.join('\n'),
        message: `docs: add SRE recommendations for ${alert.incident_id}`,
      });
    } catch (err) {
      console.warn(`   Warning: failed to commit recommendations: ${err.message}`);
    }
  }

  return paths;
}
